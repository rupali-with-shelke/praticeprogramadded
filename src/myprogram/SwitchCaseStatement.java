package myprogram;

public class SwitchCaseStatement {
     
	//WAP FOR VOWEL= a,e,i,o,u
	
	public static void main(String[] args) {
 
		char c = 3;//declaration of char
		switch(c)
		{
		case 1 : System.out.println(c='a');
		         break;
		case 2 :  System.out.println(c='e');
		         break;
		case 3 : System.out.println(c='i');
		         break;
		case 4 : System.out.println(c='o');
		         break;
		default: System.out.println(c='u');
		
		
		}
			
	}

}
