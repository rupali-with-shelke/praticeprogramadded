package myprogram;

public class ConCatenationOperator {

	public static void main(String[] args) {
		
		int a =10;
		int b =15;
		int c;
		System.out.println(10+10);//Arihmatic operator
		System.out.println("10+10");//String only
		System.out.println("10"+"10");
		c =a + b;
		System.out.println(c);
		
		c+=3;//Shortcut operator
		System.out.println(c);//25+3
	}

}
