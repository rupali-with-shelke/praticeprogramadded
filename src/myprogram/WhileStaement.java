package myprogram;

public class WhileStaement {
  //PRINT NO 1 TO 10 USING WHILE LOOP
	public static void main(String[] args) {
	
		int i =1;
		System.out.println("--------start--------");
		while(i<=10) {
			
		System.out.println(i);	
	    i++;
		
		}
		
		System.out.println("--------end-----------");

	}

}
